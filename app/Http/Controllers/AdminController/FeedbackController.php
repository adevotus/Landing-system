     */
    public function destroy($id)
    {
        $feedback = Feedback::find($id);
        if (!$feedback) {
            return back()->with('error', 'blog record not found.');
            // return redirect()->route('/hr/staff')->with('error', 'Staff record not found.');
        }

        $feedback->delete();
        $feedback = Feedback::get();
